import numpy as np
from scipy.fft import fft

#######################################################################################################################
# Function
#######################################################################################################################
def calcFreqB4(s, xs, timeAc, timeDc):
    ###################################################################################################################
    # Initialisation
    ###################################################################################################################
    # ==============================================================================
    # Parameters
    # ==============================================================================
    N = int(len(s))
    
    # ==============================================================================
    # Variables
    # ==============================================================================
    freqAc = {}
    freqDc = {}
    freqSw = {}
    
    ###################################################################################################################
    # Calculation
    ###################################################################################################################
    # ==============================================================================
    # AC-Side
    # ==============================================================================
    # ------------------------------------------
    # Switching States
    # ------------------------------------------
    # Sequence
    Y = np.abs(fft(s)/N)[0:int(N/2)]
    Y[1:-2] = 2*Y[1:-2]
    freqSw['Sa'] = Y
    
    # Sampled Reference
    Y = np.abs(fft(xs)/N)[0:int(N/2)]
    Y[1:-2] = 2*Y[1:-2]
    freqSw['Xas'] = Y

    # ------------------------------------------
    # Phase
    # ------------------------------------------
    # Current
    Y = np.abs(fft(timeAc['i_a'])/N)[0:int(N/2)]
    Y[1:-2] = 2*Y[1:-2]
    freqAc['I_a'] = Y
    
    # Voltage
    Y = np.abs(fft(timeAc['v_ab'])/N)[0:int(N/2)]
    Y[1:-2] = 2*Y[1:-2]
    freqAc['V_ab'] = Y
    
    # ==============================================================================
    # DC-Side
    # ==============================================================================
    # ------------------------------------------
    # DC-Link
    # ------------------------------------------
    # Current
    Y = np.abs(fft(timeDc['i_dc'])/N)[0:int(N/2)]
    Y[1:-2] = 2*Y[1:-2]
    freqDc['I_dc'] = Y
    
    # Voltage
    Y = np.abs(fft(timeDc['v_dc'])/N)[0:int(N/2)]
    Y[1:-2] = 2*Y[1:-2]
    freqDc['V_dc'] = Y

    ###################################################################################################################
    # Return
    ###################################################################################################################
    return [freqSw, freqAc, freqDc]